        var users = []
        fetch("https://jsonplaceholder.typicode.com/users")
            .then(response => response.json())
            .then(data => {
                users = data.map(user => ({
                    id: user.id,
                    name: user.name,
                    counter: 0
                }))
                return fetch("https://jsonplaceholder.typicode.com/posts");
            })
            .then(response => response.json())
            .then(postsData => {
                postsData.forEach(post => {
                    const user = users.find(user => user.id === post.userId);
                    if (user) {
                        user.counter++;
                    }
                });
                createChart();
            })
            
